package net.huadong.tech.ship.entity;

public class ShipReportExcel {
	private String jgDate;
	private String brandNam;
	private String num;
	public String getJgDate() {
		return jgDate;
	}
	public void setJgDate(String jgDate) {
		this.jgDate = jgDate;
	}
	public String getBrandNam() {
		return brandNam;
	}
	public void setBrandNam(String brandNam) {
		this.brandNam = brandNam;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	
}
