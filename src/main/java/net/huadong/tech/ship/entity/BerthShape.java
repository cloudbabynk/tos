package net.huadong.tech.ship.entity;

public class BerthShape {
	private Object shape;
	private Object image;
	private String flag;
	private String shipStat;
	private String text;
	private String remarks;
	private String shipNo;
	private String berthWay;
	private String shipLongNum;
	public Object getShape() {
		return shape;
	}
	public void setShape(Object shape) {
		this.shape = shape;
	}
	public Object getImage() {
		return image;
	}
	public void setImage(Object image) {
		this.image = image;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getShipStat() {
		return shipStat;
	}
	public void setShipStat(String shipStat) {
		this.shipStat = shipStat;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getShipNo() {
		return shipNo;
	}
	public void setShipNo(String shipNo) {
		this.shipNo = shipNo;
	}
	public String getBerthWay() {
		return berthWay;
	}
	public void setBerthWay(String berthWay) {
		this.berthWay = berthWay;
	}
	public String getShipLongNum() {
		return shipLongNum;
	}
	public void setShipLongNum(String shipLongNum) {
		this.shipLongNum = shipLongNum;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
}
