package net.huadong.tech.inter.entity;

public class DailyContractPlan {
	private String itransportId;
	private String informId;
	private String fleetCode;
	private String transportNo;
	private String recNam;
	private String recTim;
	private String updNam;
	private String updTim;
	public String getItransportId() {
		return itransportId;
	}
	public void setItransportId(String itransportId) {
		this.itransportId = itransportId;
	}
	public String getInformId() {
		return informId;
	}
	public void setInformId(String informId) {
		this.informId = informId;
	}
	public String getFleetCode() {
		return fleetCode;
	}
	public void setFleetCode(String fleetCode) {
		this.fleetCode = fleetCode;
	}
	public String getTransportNo() {
		return transportNo;
	}
	public void setTransportNo(String transportNo) {
		this.transportNo = transportNo;
	}
	public String getRecNam() {
		return recNam;
	}
	public void setRecNam(String recNam) {
		this.recNam = recNam;
	}
	public String getRecTim() {
		return recTim;
	}
	public void setRecTim(String recTim) {
		this.recTim = recTim;
	}
	public String getUpdNam() {
		return updNam;
	}
	public void setUpdNam(String updNam) {
		this.updNam = updNam;
	}
	public String getUpdTim() {
		return updTim;
	}
	public void setUpdTim(String updTim) {
		this.updTim = updTim;
	}
	
}
